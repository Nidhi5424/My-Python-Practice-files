my_list = [1, 2, 3, 4]

print("As set:", set(my_list))
print("\nAs tuple:", tuple(my_list))

my_tuple = (5, 6, 7)
print("\nTuple to list:", list(my_tuple))

my_set = {8, 9, 10}
print("\nSet to list:", list(my_set))
print("\nSet to tuple:", tuple(my_set))

print("\nNote: Sets remove duplicates and do not maintain order.")
