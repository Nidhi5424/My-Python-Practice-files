keys_list = list(my_dict.keys())
keys_set = set(my_dict.keys())

values_list = list(my_dict.values())
values_tuple = tuple(my_dict.values())

print("Keys as list:", keys_list)
print("\nKeys as set:", keys_set)
print("\nValues as list:", values_list)
print("\nValues as tuple:", values_tuple)
